names=[]
namelen=0

import random
def toname(data):
	data=str(data)
	val=random.Random(data).random()
	names_pos=int(val*(namelen-1))
	return names[names_pos]

class TonameMemory():
	def __init__(self,nameslist=names,seed="deterministic8g34ji"):
		self.nameshuffled=[k for k,v in enumerate(names)]
		random.Random(seed).shuffle(self.nameshuffled)
		self.names=nameslist
		self.memory={}

def toname_mem(data,db):	
	data=str(data)
	ret = db.memory.get(data)
	if not ret:
		nameid=len(db.memory)
		try:
			nameid=db.nameshuffled[nameid]
		except:
			raise ValueError("Memory full")

		ret=names[nameid]
		db.memory[data]=ret

	return ret


